#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	if (!s)
		return (NULL);
	i = 0;
	while (i < n)
	{
		if (((t_uchar*)s)[i] == (t_uchar)c)
			break ;
		i++;
	}
	return ((i == n) ? NULL : (void*)(s + i));
}
